def _english_system_table_select(
    *,
    exclude_deleted: bool = True,
    local_match: bool | None = None,
) -> list[dict[str, Any]]:
    """查詢 English_System。exclude_deleted 時僅 deleted=false。local_match 指定時僅回傳該 local。依 created_at 升序。"""
    supabase = get_supabase()
    q = supabase.table("English_System").select("*")
    if exclude_deleted:
        q = q.eq("deleted", False)
    if local_match is not None:
        q = q.eq("local", local_match)
    q = q.order("created_at", desc=False)
    resp = q.execute()
    return resp.data or []


def _phases_by_english_system_id(system_ids: list[int]) -> dict[int, list[dict[str, Any]]]:
    if not system_ids:
        return {}
    supabase = get_supabase()
    resp = (
        supabase.table("English_System_Phase")
        .select("*")
        .in_("english_system_id", system_ids)
        .order("created_at", desc=False)
        .execute()
    )
    rows = resp.data or []
    out: dict[int, list[dict[str, Any]]] = {sid: [] for sid in system_ids}
    for row in rows:
        sid = row.get("english_system_id")
        if sid is not None:
            try:
                out.setdefault(int(sid), []).append(row)
            except (TypeError, ValueError):
                pass
    return out


