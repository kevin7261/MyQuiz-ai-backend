def _validate_english_system_grade_quiz_phase(
    supabase: Any,
    *,
    english_system_id: int,
    eff_tab: str,
    person_id: str,
    english_system_quiz_phase_id: int,
    english_system_quiz_id: int,
) -> None:
    """若 english_system_quiz_phase_id／english_system_quiz_id > 0，驗證 Phase／Quiz 列與 english_system／tab／person 一致。"""
    if english_system_quiz_phase_id > 0:
        ph = (
            supabase.table("English_System_Phase")
            .select("english_system_quiz_phase_id")
            .eq("english_system_quiz_phase_id", english_system_quiz_phase_id)
            .eq("english_system_id", english_system_id)
            .eq("english_system_tab_id", eff_tab)
            .eq("person_id", person_id)
            .limit(1)
            .execute()
        )
        if not ph.data:
            raise HTTPException(
                status_code=404,
                detail="找不到符合之 English_System_Phase（請檢查 english_system_quiz_phase_id）",
            )
    if english_system_quiz_id <= 0:
        return
    r = (
        supabase.table("English_System_Quiz")
        .select(
            "english_system_quiz_id, english_system_id, english_system_tab_id, person_id, english_system_quiz_phase_id"
        )
        .eq("english_system_quiz_id", english_system_quiz_id)
        .limit(1)
        .execute()
    )
    if not r.data:
        raise HTTPException(
            status_code=404,
            detail=f"找不到 english_system_quiz_id={english_system_quiz_id} 的 English_System_Quiz",
        )
    row = r.data[0]
    if int(row.get("english_system_id") or 0) != english_system_id:
        raise HTTPException(status_code=400, detail="english_system_quiz_id 與 english_system_id 不符")
    if (row.get("english_system_tab_id") or "").strip() != eff_tab:
        raise HTTPException(status_code=400, detail="english_system_quiz_id 與 english_system_tab_id 不符")
    if (row.get("person_id") or "").strip() != person_id:
        raise HTTPException(status_code=403, detail="無權對該題評分")
    qph = row.get("english_system_quiz_phase_id")
    try:
        qph_i = int(qph) if qph is not None else 0
    except (TypeError, ValueError):
        qph_i = 0
    if english_system_quiz_phase_id > 0 and qph_i != english_system_quiz_phase_id:
        raise HTTPException(
            status_code=400,
            detail="english_system_quiz_id 所屬 phase 與 english_system_quiz_phase_id 不符",
        )


def _run_english_system_grade_llm(
    api_key: str,
    *,
    quiz_text: str,
    critique_user_prompt_instruction: str,
    quiz_content: str,
    quiz_answer: str,
) -> tuple[str, dict[str, Any]]:
    """Chat system = quiz_text；user = critique_user_prompt_instruction + 題目／作答與與 POST /rag/tab/quiz/grade 相同之 JSON 輸出規格。"""
    system_content = (quiz_text or "").strip() or "你是測驗批改助理。"
    user_instruction = (critique_user_prompt_instruction or "").strip()
    course_name = get_course_name_for_prompt()
    user_content = f"""{user_instruction}

【批改作業】
你正在批改「{course_name}」相關測驗。

【評分規範】
請根據「測驗題目」與教材／脈絡（見 system 訊息），評估「學生答案」是否正確。
測驗題目：{quiz_content}
學生答案：{quiz_answer}

【重要限制】
請使用繁體中文撰寫評語（quiz_comments 陣列中每一則）。
【評分標準】
0-5分，一定是整數（quiz_grade）。
0: 完全錯誤或未作答。
1: 只有少量內容正確。
2: 大幅缺漏，只有部分內容正確。
3: 部分正確，但有大幅缺漏。
4: 大致正確，略有不足。
5: 完全正確且完整。
【輸出 JSON】
請以 JSON 格式回傳：
{{ "quiz_grade": int,
"quiz_comments": str[] }}
"""
    client = OpenAI(api_key=api_key)
    response = client.chat.completions.create(
        model=_ENGLISH_TAB_PHASE_LLM_MODEL,
        messages=[
            {"role": "system", "content": system_content},
            {"role": "user", "content": user_content},
        ],
        response_format={"type": "json_object"},
        temperature=0.3,
    )
    llm_raw = response.choices[0].message.content or ""
    try:
        llm_json = json.loads(llm_raw)
    except json.JSONDecodeError:
        llm_json = {}
    if not isinstance(llm_json, dict):
        llm_json = {}
    _normalize_grading_llm_json(llm_json)
    return llm_raw, llm_json


