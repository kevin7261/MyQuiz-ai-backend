def english_system_tab_build_system(body: EnglishSystemTabBuildSystemRequest, caller_person_id: PersonId):
    """
    依 `system_tab_id` 與 `person_id` 更新該筆 English_System（deleted=false）之 **quiz_type、quiz_text、quiz_mp3_filename、quiz_youtube_url**，
    並將此四欄之值以相同鍵名合併寫入 **system_metadata**（保留既有 JSON 其他鍵，例如 `transcript/audio` 寫入之 `english_audio`）。
    `quiz_type` 未於 body 傳入時預設為 **0**（與資料表預設一致）。
    須已存在對應列（例如已呼叫 `POST /english_system/tab/create` 或 `POST /english_system/transcript/audio` 自動建列）。
    請求格式為 JSON body，對齊 `POST /rag/tab/build-rag-zip` 之「以 body 識別 tab 並寫回資料表」用法（本端點不建 RAG、不串流 NDJSON）。
    """
    fid = (body.system_tab_id or "").strip()
    if not fid or "/" in fid or "\\" in fid:
        raise HTTPException(status_code=400, detail="無效的 system_tab_id")
    pid = (body.person_id or "").strip()
    if not pid:
        raise HTTPException(status_code=400, detail="請傳入 person_id")
    if pid != caller_person_id:
        raise HTTPException(status_code=400, detail="body 的 person_id 與 query 不一致")

    mp3 = body.quiz_mp3_filename
    if mp3 is not None and len(mp3) > _VARCHAR255_MAX:
        raise HTTPException(
            status_code=400,
            detail=f"quiz_mp3_filename 長度不可超過 {_VARCHAR255_MAX}",
        )
    yt = body.quiz_youtube_url
    if yt is not None and len(yt) > _VARCHAR255_MAX:
        raise HTTPException(
            status_code=400,
            detail=f"quiz_youtube_url 長度不可超過 {_VARCHAR255_MAX}",
        )

    ts = now_taipei_iso()
    quiz_meta = {
        "quiz_type": body.quiz_type,
        "quiz_text": body.quiz_text,
        "quiz_mp3_filename": mp3,
        "quiz_youtube_url": yt,
    }

    try:
        supabase = get_supabase()
        sel = (
            supabase.table("English_System")
            .select("system_id, system_tab_id, person_id, system_metadata")
            .eq("system_tab_id", fid)
            .eq("person_id", pid)
            .eq("deleted", False)
            .limit(1)
            .execute()
        )
        if not sel.data or len(sel.data) == 0:
            raise HTTPException(
                status_code=404,
                detail="找不到該 system_tab_id 與 person_id 之 English_System，請先呼叫 POST /english_system/tab/create",
            )
        row0 = sel.data[0]
        sid = row0["system_id"]
        prev_sys = row0.get("system_metadata")
        base_meta = prev_sys if isinstance(prev_sys, dict) else {}
        merged_meta = {**base_meta, **quiz_meta}
        update_payload: dict[str, Any] = {
            **quiz_meta,
            "system_metadata": to_json_safe(merged_meta),
            "updated_at": ts,
        }
        supabase.table("English_System").update(update_payload).eq("system_id", sid).eq("deleted", False).execute()
        meta_out = to_json_safe(merged_meta)
        return EnglishSystemTabBuildSystemResponse(
            system_id=sid,
            system_tab_id=fid,
            person_id=pid,
            quiz_type=body.quiz_type,
            quiz_text=body.quiz_text,
            quiz_mp3_filename=mp3,
            quiz_youtube_url=yt,
            system_metadata=meta_out if isinstance(meta_out, dict) else None,
            updated_at=ts,
        )
    except HTTPException:
        raise
    except Exception as e:
        logging.exception("POST /english_system/tab/build-system 錯誤")
        raise HTTPException(status_code=500, detail=str(e)) from e


