def _enrich_english_system_rows_with_relations(data: list[dict[str, Any]]) -> None:
    """併入 English_System_Phase（phases，每段含 quizzes）、每題一筆 English_System_Answer（同 GET /rag/tabs 取第一筆），頂層 answers 為該單元全部作答。"""
    if not data:
        return
    system_ids: list[int] = []
    for row in data:
        sid = row.get("system_id")
        if sid is not None:
            try:
                system_ids.append(int(sid))
            except (TypeError, ValueError):
                pass
    system_ids = list(dict.fromkeys(system_ids))
    if not system_ids:
        return
    phases_by = _phases_by_english_system_id(system_ids)
    quizzes_by = _quizzes_by_english_system_id(system_ids)
    answers_by = _answers_by_english_system_id(system_ids)
    all_quiz_ids: list[int] = []
    for sid in system_ids:
        for q in quizzes_by.get(sid, []):
            qid = q.get("english_system_quiz_id")
            if qid is not None:
                try:
                    all_quiz_ids.append(int(qid))
                except (TypeError, ValueError):
                    pass
    all_quiz_ids = list(dict.fromkeys(all_quiz_ids))
    answers_by_qid = _answers_by_english_system_quiz_ids(all_quiz_ids)

    for row in data:
        sid = row.get("system_id")
        if sid is None:
            continue
        try:
            sid_int = int(sid)
        except (TypeError, ValueError):
            continue
        phases = phases_by.get(sid_int, [])
        all_quizzes = quizzes_by.get(sid_int, [])
        phase_id_set: set[int] = set()
        for p in phases:
            pid = p.get("english_system_quiz_phase_id")
            if pid is not None:
                try:
                    phase_id_set.add(int(pid))
                except (TypeError, ValueError):
                    pass
        by_phase: dict[int, list[dict[str, Any]]] = {}
        orphans: list[dict[str, Any]] = []
        for q in all_quizzes:
            qpid = q.get("english_system_quiz_phase_id")
            try:
                qpid_i = int(qpid) if qpid is not None else None
            except (TypeError, ValueError):
                qpid_i = None
            if qpid_i is not None and qpid_i in phase_id_set:
                by_phase.setdefault(qpid_i, []).append(q)
            else:
                orphans.append(q)
        for ph in phases:
            pid = ph.get("english_system_quiz_phase_id")
            try:
                pid_i = int(pid) if pid is not None else None
            except (TypeError, ValueError):
                pid_i = None
            p_quizzes = by_phase.get(pid_i, []) if pid_i is not None else []
            for quiz in p_quizzes:
                qid = quiz.get("english_system_quiz_id")
                try:
                    qid_i = int(qid) if qid is not None else None
                except (TypeError, ValueError):
                    qid_i = None
                raw = (answers_by_qid.get(qid_i, []) or []) if qid_i is not None else []
                quiz["answers"] = raw[:1]
            ph["quizzes"] = p_quizzes
        for quiz in orphans:
            qid = quiz.get("english_system_quiz_id")
            try:
                qid_i = int(qid) if qid is not None else None
            except (TypeError, ValueError):
                qid_i = None
            raw = (answers_by_qid.get(qid_i, []) or []) if qid_i is not None else []
            quiz["answers"] = raw[:1]
        row["phases"] = phases
        row["quizzes"] = orphans
        row["answers"] = answers_by.get(sid_int, [])


